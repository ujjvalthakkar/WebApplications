    private void viewResumeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewResumeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = resumesJTable.getSelectedRow();
        
        if (selectedRow >= 0) {
            resumeJPanel.setVisible(true);
            Resume rs = (Resume) resumesJTable.getValueAt(selectedRow, 0);
            // Setting all text box values 
            String firstName = rs.getFirstName();
            firstNameTxtField.setText(firstName);
            String lastName = rs.getLastName();
            lastNameTxtField.setText(lastName);
            String addLine1 = rs.getAddressLine1();
            addrLine1TxtField.setText(addLine1);
            String addLine2 = rs.getAddressLine2();
            addrLine2TxtField.setText(addLine2);
            String city = rs.getCity();
            cityTxtField.setText(city);
            String state = rs.getState();
            stateTxtField.setText(state);
            String country = rs.getCountry();
            countryTxtField.setText(country);
            String zip = rs.getZipcode();
            zipTxtField.setText(zip);
            String contactNumb = rs.getContactNumb();
            contactTxtField.setText(contactNumb);
            String emailId = rs.getEmailId();
            emailTxtField.setText(emailId);
            String objectiveStatement = rs.getObjectiveStatement();
            objStmntjTextArea.setText(objectiveStatement);
            String affliation = rs.getAffiliation();
            affiliationTxtField.setText(affliation);
            
            String imagePath = rs.getImagePath();
            ImageIcon selectedImage = new ImageIcon(imagePath);
            Image img=selectedImage.getImage();
            Image scaledImg=img.getScaledInstance(displayImageJLabel.getWidth(),displayImageJLabel.getHeight(),Image.SCALE_SMOOTH);
            ImageIcon scaledImage=new ImageIcon(scaledImg);
            displayImageJLabel.setIcon(scaledImage);
            
            dobTxtJLabel.setText(rs.getDob());
            String univName = rs.getUnivName();
            universityJTextField.setText(univName);
            String dateOfCompletion = rs.getDateOfCompletion();
            completionDateJTextField.setText(dateOfCompletion);
            String major = rs.getMajor();
            majorJTextField.setText(major);
            String degreeType = rs.getDegreeType();
            degreeTypeJTextField.setText(degreeType);
            String skillSet = rs.getSkill();
            skillSetTextJLabel.setText(skillSet);
            String achievements = rs.getAchievements();
            achievementsJTextArea.setText(achievements);
            gpaJTextField.setText(rs.getGpa());
            experienceJTextArea.setText(rs.getExperience());
            genderJLabelText.setText(rs.getGender());
            citizenshipJTextField.setText(rs.getCitizenship());
            gradStatusTextJLabel.setText(rs.getGraduationStatus());
            
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }

    }//GEN-LAST:event_viewResumeJButtonActionPerformed

    private void deleteResumeJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteResumeJButtonActionPerformed
        // TODO add your handling code here:
        int selectedRow = resumesJTable.getSelectedRow();
        if (selectedRow >= 0) {
            int result = JOptionPane.showConfirmDialog(this, "Confirm Delete?", "Warning", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                Resume rs = (Resume) resumesJTable.getValueAt(selectedRow, 0);
                resumeRepo.deleteResumes(rs);
                JOptionPane.showMessageDialog(this, "Selected Resume has been Deleted!");
                populateTable();
                clearFields();
                resumeJPanel.setVisible(false);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please select a row!");
        }
    }//GEN-LAST:event_deleteResumeJButtonActionPerformed

