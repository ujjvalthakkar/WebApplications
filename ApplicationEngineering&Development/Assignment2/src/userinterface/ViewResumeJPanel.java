    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) resumesJTable.getModel();
        dtm.setRowCount(0); // Emptied the table
        for (Resume rs : resumeRepo.getResumeRepository()) {
            Object row[] = new Object[3];
            row[0] = rs;
            row[1] = rs.getLastName();
            row[2] = rs.getSkill();
            dtm.addRow(row);
        }
    }

