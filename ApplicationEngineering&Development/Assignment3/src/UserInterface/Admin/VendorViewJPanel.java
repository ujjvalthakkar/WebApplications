    private void btnDeleteVendorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteVendorActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblVendor.getSelectedRow();
        if (selectedRow >= 0) {
            int result = JOptionPane.showConfirmDialog(this, "Confirm Delete?", "Warning", JOptionPane.YES_NO_OPTION);
            if (result == JOptionPane.YES_OPTION) {
                 Vendor v = (Vendor) tblVendor.getValueAt(selectedRow, 0);
                vendorList.deleteVendor(v);
                JOptionPane.showMessageDialog(this, "Selected Vendor has been Deleted!");
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(this, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_btnDeleteVendorActionPerformed


