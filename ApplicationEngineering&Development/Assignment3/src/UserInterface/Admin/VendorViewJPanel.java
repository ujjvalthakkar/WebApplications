    public void populateTable() {
        DefaultTableModel dtm = (DefaultTableModel) tblVendor.getModel();
        dtm.setRowCount(0); // Empties the table
        for (Vendor v : vendorList.getVendorList()) {
            Object row[] = new Object[2];
            row[0] = v;
            row[1] = v.getId();
            dtm.addRow(row);
        }
        
    }

